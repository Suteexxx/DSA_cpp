#include<iostream>
using namespace std;
int main() {
    int a=10, b=20;
    cout<<"a + b = "<<a+b<<endl; // arithmetic operator
    cout<<"a - b = "<<a-b<<endl;
    cout<<"a * b = "<<a*b<<endl;
    cout<<"a / b = "<<a/b<<endl;
    cout<<"a % b = "<<a%b<<endl;
    cout<<(10>20)<<endl; // relational operator
    cout<<(10<20)<<endl;
    cout<<(10>=20)<<endl;
    cout<<(10<=20)<<endl;
    cout<<(10==20)<<endl;
    cout<<(10!=20)<<endl;
    return 0;   
}
